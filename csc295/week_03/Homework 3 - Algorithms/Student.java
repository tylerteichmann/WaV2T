// Student class
public class Student {
    // Property for student's name.
    String name;
    // Property for the student's gpa.
    double gpa;
    // Constructor for student class to set name and GPA
    public Student(String name, double gpa) {
        // Set name to the value passed in for the fist argument
        this.name = name;
        // Set gpa to the value passed in for the second argument
        this.gpa = gpa;
    }
}
